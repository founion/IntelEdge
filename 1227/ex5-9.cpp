#include <iostream>

using namespace std;

class Circle
{
public:
	Circle() { radius = 1; }
	Circle(const Circle& c) { this->radius = c.radius; cout << "복사 생성자 실행 radius = " << radius << endl; }
	Circle(int radius) { this->radius = radius; }
	double getArea() { return 3.14 * radius * radius; }
private:
	int radius;
};

int main() {
	Circle src(30);
	Circle dest(src);

	cout << "원본의 면적 = " << src.getArea() << endl;
	cout << "사본의 면적 = " << dest.getArea() << endl;
}